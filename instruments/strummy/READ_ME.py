# Standard midi controller
# 
# Microcontroller: Waveshare ESP32-S3-Zero
# PCB: Designed May 2025 by Ian Hattwick
# This READ_ME: May 25 by Ian Hattwick
# 
# This midi device has:
# - four buttons
# - four potentiometers.
# - standard six axis IMU. - either LSM6DS3 or BMI160
# - RGB LED with separate pins for RGB
# 
# Button pins: [9,8,2,7]
# Potentiometer pins: [6,3,4,5]
# I2C pins: [44,43] SDA,SCL ??
# LED pins: [13,12,11] RGB

